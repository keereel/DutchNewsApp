import UIKit

extension UIColor {
    class var blueDark: UIColor {
        return UIColor(red: 0, green: 0.388, blue: 0.561, alpha: 1)
    }

    class var blueLight: UIColor {
        return UIColor(red: 0.067, green: 0.584, blue: 0.765, alpha: 1)
    }

    static var bluePale: UIColor {
        return UIColor(red: 0.949, green: 0.965, blue: 0.976, alpha: 1)
    }

    static var blueMiddle: UIColor {
        return UIColor(red: 0.227, green: 0.51, blue: 0.776, alpha: 1)
    }

    class var blueLink: UIColor {
        return UIColor(red: 0.098, green: 0.388, blue: 0.929, alpha: 1)
    }
}
